class SampleRam(Elaboratable):
	def __init__(self):
		# -------------------------------------
		# Inputs

		self.waddr = Signal(9)
		self.wdata = Signal(4)
		self.we    = Signal(1)

		self.raddr = Signal(9)

		# -------------------------------------
		# Outputs

		self.rdata = Signal(4)

	def elaborate(self, platform: Platform) -> Module:
		m = Module()

		# -------------------------------------
		# Submodules

		mem = Memory(width = 4, depth = 512, init = DUMMY_RAM)
		m.submodules.rdport = rdport = mem.read_port()
		m.submodules.wrport = wrport = mem.write_port()

		# -------------------------------------
		# Combinational Logic

		m.d.comb += [
			wrport.addr.eq(self.waddr),
			wrport.data.eq(self.wdata),
			wrport.en.  eq(self.we),

			rdport.addr.eq(self.raddr),
			self.rdata. eq(rdport.data),
		]

		return m
